package com.xiyao.security.enums;

/**
 * 三员类型枚举
 * <p>
 * 用于等保合规的三员管理机制，将用户分为四种类型：
 * 普通用户、系统管理员、安全管理员、审计管理员。
 *
 * <p>
 * <b>三员管理说明：</b>
 * <ul>
 *     <li>系统管理员：负责系统日常运行维护</li>
 *     <li>安全管理员：负责安全策略配置和权限管理</li>
 *     <li>审计管理员：负责日志审计和操作记录查看</li>
 *     <li>普通用户：普通业务操作人员</li>
 * </ul>
 *
 * <p>
 * <b>安全等级要求：</b>
 * 等保合规要求实现三员分立，相互制约，防止权限过度集中。
 *
 * @author xiyao
 */
public enum AdminType {

    /**
     * 普通用户
     * <p>
     * 普通业务操作人员，只能访问被授权的业务功能。
     */
    NormalUser,

    /**
     * 系统管理员
     * <p>
     * 负责系统的日常运行维护，包括服务器运维、应用部署等。
     */
    SystemAdmin,

    /**
     * 安全管理员
     * <p>
     * 负责安全策略配置、用户权限管理、安全审计等。
     */
    SecurityAdmin,

    /**
     * 审计管理员
     * <p>
     * 负责查看和审计系统操作日志、安全事件等。
     */
    AuditAdmin,
}