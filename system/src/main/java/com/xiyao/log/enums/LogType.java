package com.xiyao.log.enums;

/**
 * 日志类型枚举
 * <p>
 * 用于区分操作日志和审计日志，决定日志记录的存储表和安全策略。
 *
 * @author xiyao
 */
public enum LogType {

    /**
     * 操作日志（业务追踪）
     * <p>
     * 记录日常业务操作，用于问题排查和业务追踪。
     * 无需哈希链防篡改，普通人员可查询。
     */
    OPERATION,

    /**
     * 审计日志（合规审计）
     * <p>
     * 记录敏感操作，用于等保合规审计。
     * 必须有哈希链防篡改，仅审计管理员可查询。
     */
    AUDIT
}